package com.uhutown.filecreator.signalfiles;

public class SoundPropertyParser {

	private String name;
	private int length;

	public void setName(String name) {
		this.name = name;
	}

	public void setLength(int length) {
		this.length = length;
	}

	public String getName() {
		return name;
	}

	public int getLength() {
		return length;
	}

}
